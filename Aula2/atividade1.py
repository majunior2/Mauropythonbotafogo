"""
Escreva um programa que leia um valor em metros e exiba convertido em milímetros.
"""
# Entrada
metros = float(input("Digite o valor em Metros: "))

# Processamento (1 metro = 1000 milímetros)
milimetros = metros * 1000

# Saída
print(f'{metros} metros é igual a {milimetros} milímetros.')